export function setLang(lang) {
  currentLang.value = lang === 'eng' ? 'eng' : 'ua'
  localStorage.setItem(LANG_KEY, currentLang.value)
}

export function useI18n() {
  const t = (key) => messages[currentLang.value]?.[key] || messages.ua[key] || key
  return {
    lang: computed(() => currentLang.value),
    t,
    setLang,
  }
}
